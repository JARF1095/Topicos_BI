# Operadores de asignacion

# Operador igual "="
a = 15     # Asigno el valor de 15 a la variable a
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador incremental "+=" 
# Permite incrementar en x unidades una variable
a += 5
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador decremental "-="
a -= 15
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador multiplicacion "*="
a *= 3
print(f"El valor de asignado a la variable 'a' es {a}")


# Operador division "/="
a /= 7.5
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador potencia "**="
a **= 5
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador parte entera "//="
a //= 10
print(f"El valor de asignado a la variable 'a' es {a}")

# Operador residuo "%="
a %= 2
print(f"El valor de asignado a la variable 'a' es {a}")
