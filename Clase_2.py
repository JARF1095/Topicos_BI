# Operaciones matematicas basicas en Python
# Suma
a = 8.5
b = 14
suma = a + b
print(f"La suma de {a} + {b} = {suma}")

# Resta
a = 15
b = 18
resta = a - b
print(f"La resta de {a} - {b} = {resta}")

# Multiplicacion
a = 7
b = -15
multiplicacion = a * b
print(f"La multiplicacion de {a} * {b} = {multiplicacion}")

# Division
a = 8
b = 2
division = a / b
print(f"La division de {a} / {b} = {division:.2f}")

# Potenciacion
a = 4
b = - 0.5
potenciacion = a ** b
print(f"La potencia de {a} ^ {b} = {potenciacion:.2f}")

# Parte entera
a = 25
b = 7
parte_entera = a // b
print(f"La parte entera {a} / {b} = {parte_entera}")

# Parte residual
a = 8
b = 3
residuo = a % b
print(f"Residuo de {a} / {b} = {residuo}")