# Tipos de variables
Entero = 5    # Esta es una variable entera
print(f"Esta es una variable del tipo entera: {Entero}")
Flotante = 2.5   # Esta es una variable flotante
print(f"Esta es una variable del tipo flotane: {Flotante}")
Caracter = 'A'   # Esta es una variable caracter
print(f"Esta es una 'variable' del tipo caracter: {Caracter}")
String = "Hola"  # Esta es una variable string
print(f"Esta es una variable del tipo string: {String}")
Verdadero = True # Variable booleana (Verdad)
Falso = False    # Variable booleana (Mentira)
print(f"Estas son variables booleanas: {Verdadero} y {Falso}")