# Operadores de comparacion
# Operador de igualdad
variable_1 = 15
variable_2 = 15

print(variable_1 == variable_2)

# Operador de no igualdad
variable_1 = 25
variable_2 = 25

print(variable_1 != variable_2)

# Operador mayor que
variable_1 = 85
variable_2 = 15

print(variable_1 > variable_2)

# Operador de menor que
variable_1 = 56
variable_2 = 56

print(variable_1 < variable_2)

# Operador mayor o igual que
variable_1 = 87.000001
variable_2 = 87

print(variable_1 >= variable_2)

# Operador menor o igual que
variable_1 = -52
variable_2 = -10

print(f'El valor de {variable_1} es menor o igual que el valor de {variable_2}: {variable_1 <= variable_2}')
